int		ft_sqrt(int nb)
{
	double sqrt;

	sqrt = 1;
	while (sqrt * sqrt <= nb)
	{
		if (sqrt * sqrt == nb)
			return ((int)sqrt);
		sqrt++;
	}
	return (0);
}
